    private void confobtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confobtnActionPerformed
      if(roomno.getText().equals(" ")){
          JOptionPane.showMessageDialog(this, " All Feilds Should Be Filled");
          roomno.requestFocus();
      }// TODO add your handling code here:
      else if(price.getText().equals(" ")){
          JOptionPane.showMessageDialog(this, " All Feilds Should Be Filled");
          price.requestFocus();
      }
      else{
          try {
              pst=con.prepareStatement("select * from room_details where roomno=?");
              pst.setString(1, roomno.getText());
              rs=pst.executeQuery();
              if(rs.next()){
               JOptionPane.showMessageDialog(this, " Room No Already Exist");  
              }
              else{
                  pst=con.prepareStatement("insert into room_details(roomno,Room_type,Bed_type,Price,status)values(?,?,?,?,?)");
                   pst.setString(1, roomno.getText());
                    pst.setString(2, typecombo.getItemAt(typecombo.getSelectedIndex()));
                     pst.setString(3, bedcombo.getItemAt(bedcombo.getSelectedIndex()));
                      pst.setString(4, price.getText());
                     pst.setString(5, statuscombo.getItemAt(statuscombo.getSelectedIndex()));
                     pst.executeUpdate();
                      JOptionPane.showMessageDialog(this, " Room Added Successfully");  
                      SHowRecordintable();
                      roomno.setText("");
                      price.setText("");
              }
          } catch (SQLException ex) {
              Logger.getLogger(Manage_Rooms.class.getName()).log(Level.SEVERE, null, ex);
          }
      }
    }//GEN-LAST:event_confobtnActionPerformed

