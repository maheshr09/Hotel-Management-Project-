    public  void SHowRecordintable(){
        int c=0;
            try {
                pst=con.prepareStatement("select * from room_details");
                rs=pst.executeQuery();
                ResultSetMetaData rsmd=rs.getMetaData();
                c=rsmd.getColumnCount();
                DefaultTableModel DModel=(DefaultTableModel) jTable2.getModel();
                DModel.setRowCount(0);
                while (rs.next()) {
                    Vector column = new Vector();
                    for(int i=1;i<=c;i++){
                        column.add(rs.getString("roomno"));
                        column.add(rs.getString("Room_type"));
                        column.add(rs.getString("Bed_type"));
                        column.add(rs.getString("Price"));
                        column.add(rs.getString("status"));
                    }
                    DModel.addRow(column);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Manage_Rooms.class.getName()).log(Level.SEVERE, null, ex);
            }
    }

